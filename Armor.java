public class Armor extends Item
{
    private int armorValue;
    private int strReq;

    public int getArmorValue()
    {
        return armorValue;
    }

    public void setArmorValue(int armorValue)
    {
        this.armorValue = armorValue;
    }

    public int getStrReq()
    {
        return strReq;
    }
    
    public void setStrReq(int strReq)
    {
        this.strReq = strReq;
    }
}
