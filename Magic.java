public class Magic extends Weapon
{
    int manaCost;
    int knowReq;
    
    public int getManaCost()
    {
        return manaCost;
    }
    
    public void setManaCost(int manaCost)
    {
        this.manaCost = manaCost;
    }
    
    public int getKnowReq()
    {
        return knowReq;
    }
    
    public void setKnowReq(int knowReq)
    {
        this.knowReq = knowReq;
    }
}
